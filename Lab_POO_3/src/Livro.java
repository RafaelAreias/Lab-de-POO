public class Livro {

    private String titulo;
    private int paginas;

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String livro) {
        this.titulo = livro;
    }

    public int getPaginas() {
        return paginas;
    }

    public void setPaginas(int paginas) {
        this.paginas = paginas;
    }
}
